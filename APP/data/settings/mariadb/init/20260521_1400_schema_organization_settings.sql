CREATE TABLE IF NOT EXISTS organization_settings (
  id                  INT NOT NULL,
  name                VARCHAR(200) NULL,
  legal_name          VARCHAR(200) NULL,
  tax_id              VARCHAR(40) NULL,
  description         VARCHAR(600) NULL,
  industry            VARCHAR(120) NULL,
  email               VARCHAR(254) NULL,
  phone               VARCHAR(30) NULL,
  website             VARCHAR(500) NULL,
  address             VARCHAR(400) NULL,
  country             VARCHAR(120) NULL,
  region              VARCHAR(120) NULL,
  city                VARCHAR(120) NULL,
  postal_code         VARCHAR(40) NULL,
  contact_name        VARCHAR(200) NULL,
  contact_email       VARCHAR(254) NULL,
  contact_phone       VARCHAR(30) NULL,
  contact_position    VARCHAR(120) NULL,
  contact_department  VARCHAR(120) NULL,
  notes               TEXT NULL,
  avatar_object_id    CHAR(36) NULL,
  banner_object_id    CHAR(36) NULL,
  created_at          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by          CHAR(36) NULL,
  updated_at          DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  updated_by          CHAR(36) NULL,

  PRIMARY KEY (id),
  KEY idx_organization_settings_avatar_object_id (avatar_object_id),
  KEY idx_organization_settings_banner_object_id (banner_object_id),
  KEY idx_organization_settings_created_by (created_by),
  KEY idx_organization_settings_updated_by (updated_by),
  CONSTRAINT fk_organization_settings_avatar_object FOREIGN KEY (avatar_object_id) REFERENCES objects(id),
  CONSTRAINT fk_organization_settings_banner_object FOREIGN KEY (banner_object_id) REFERENCES objects(id),
  CONSTRAINT fk_organization_settings_created_by FOREIGN KEY (created_by) REFERENCES users(id),
  CONSTRAINT fk_organization_settings_updated_by FOREIGN KEY (updated_by) REFERENCES users(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

