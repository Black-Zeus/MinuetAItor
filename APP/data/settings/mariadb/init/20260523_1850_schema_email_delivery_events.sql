CREATE TABLE IF NOT EXISTS email_delivery_events (
  id                  CHAR(36) PRIMARY KEY,
  job_id              CHAR(36) NOT NULL,
  queue_name          VARCHAR(80) NOT NULL DEFAULT 'queue:email',
  status              VARCHAR(30) NOT NULL,
  email_kind          VARCHAR(80) NOT NULL DEFAULT 'system',
  notification_type   VARCHAR(80) NULL,
  template_id         VARCHAR(120) NULL,
  subject             VARCHAR(300) NOT NULL,
  email_type          VARCHAR(20) NOT NULL DEFAULT 'html',
  to_json             JSON NULL,
  cc_json             JSON NULL,
  bcc_json            JSON NULL,
  recipient_count     INT UNSIGNED NOT NULL DEFAULT 0,
  attachment_count    INT UNSIGNED NOT NULL DEFAULT 0,
  inline_asset_count  INT UNSIGNED NOT NULL DEFAULT 0,
  scope_type          VARCHAR(80) NULL,
  scope_id            VARCHAR(64) NULL,
  record_id           CHAR(36) NULL,
  actor_user_id       CHAR(36) NULL,
  tags_json           JSON NULL,
  metadata_json       JSON NULL,
  attempt             INT UNSIGNED NOT NULL DEFAULT 1,
  error_message       TEXT NULL,
  queued_at           DATETIME NULL,
  sent_at             DATETIME NULL,
  failed_at           DATETIME NULL,
  event_at            DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_at          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at          DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

  UNIQUE KEY uq_email_delivery_events_job (job_id),
  KEY idx_ede_status (status),
  KEY idx_ede_kind (email_kind),
  KEY idx_ede_template (template_id),
  KEY idx_ede_record (record_id),
  KEY idx_ede_scope (scope_type, scope_id),
  KEY idx_ede_queued_at (queued_at),
  KEY idx_ede_sent_at (sent_at),
  KEY idx_ede_failed_at (failed_at),
  KEY idx_ede_event_at (event_at),
  KEY idx_ede_kind_event (email_kind, event_at),
  KEY idx_ede_status_event (status, event_at),

  CONSTRAINT fk_ede_record FOREIGN KEY (record_id) REFERENCES records(id),
  CONSTRAINT fk_ede_actor FOREIGN KEY (actor_user_id) REFERENCES users(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
